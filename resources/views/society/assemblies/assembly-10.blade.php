            <div class="col-md-6 center-block lead">
              The Infectious Diseases Assembly covers all issues of pulmonary and respiratory infections with a strong focus on pneumonia and antibiotic resistance, bronchiectasis, tuberculosis and non-tuberculous mycobacteria. This assembly represents the group on Respiratory Infections (10.1) and the group on Tuberculosis (10.2). It is one of the largest scientific assemblies of the ERS. Airway and pulmonary infections play an important role for outpatient and clinical and intensive care medicine in children and adults and the topics covered by our assembly include bacterial, viral, fungal and parasitic infections. Prevention of infections and emergence of antibiotic resistance play an important role in our scientific and educational activities and in the interactions with the other scientific assemblies in the ERS. Members of the assembly, especially the junior one, are engaged in educational activities through the ERS school and in large collaborative research networks including:<br><br>
